<p><strong>Politiko</strong> (з грец.) - управління державою або громадська  діяльність, спрямована на захист соціальних чи групових інтересів;  завоювання і зміцнення державної влади; завдачі держави та кошти, шляхи  їх досягнення. У Платона і Аристотеля - наука про суспільство, державу; у  Бісмарка - "мистецтво можливого".</p>
